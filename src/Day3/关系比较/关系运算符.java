package Day3.关系比较;

public class 关系运算符 {
    public static void main(String[] args) {
        /*关系运算符 判断数据大小关系
         * 运算结果是布尔类型   true false
         * >  <  >=  <=  == !=
         * */
        boolean b = 10 > 20;
        System.out.println(b);
        System.out.println(10 == 20);
        System.out.println(10 != 20);
        boolean i = 2 <= 3;
        System.out.println(i);

    }
}
